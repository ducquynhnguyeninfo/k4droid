package com.www.k4droid_v05.model.actionbar;

public class SpinnerNavItem {

	private String title;
	private int icon;

	public SpinnerNavItem(String title, int icon) {
		this.title = title;
		this.icon = icon;
	}

	public String getTitle() {
		return title;
	}

	public int getIcon() {
		return icon;
	}
}
